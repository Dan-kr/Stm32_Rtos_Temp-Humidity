void StartSHT30Task(void *argument) {
    float temperature=0.0;
    float humidity=0.0;
    for (;;) {
        SHT30_Read(&temperature, &humidity);
        printf("Temperature: %.2f°C\n", temperature);   // Debug print to verify the value
        printf("Humidity: %.2f%% \n", humidity);     
        float readings[2] = {temperature, humidity};
        HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_5); // Blink LED
        osDelay(4000); // Every 2 seconds
    }
}



void SHT30_Read(float *temperature, float *humidity) {
    uint8_t cmd[2] = {0x2C, 0x06};
    uint8_t data[6];


    //Send measurement command
    HAL_I2C_Master_Transmit(&hi2c2, SHT30_ADDRESS, cmd, 2, HAL_MAX_DELAY);
     // Wait for measurement to complete (15ms)
    HAL_Delay(20);
    // Read data
    HAL_I2C_Master_Receive(&hi2c2, SHT30_ADDRESS, data, 6, HAL_MAX_DELAY);
    

    // Convert raw values
    uint16_t rawTemp = (data[0] << 8) | data[1];
    uint16_t rawHum = (data[3] << 8) | data[4];
    //Convert to temperature and humidity
    *temperature = -45.0f + (175.0f *(float)rawTemp / 65535.0f);
    *humidity = 100.0f * (float)rawHum / 65535.0f;

}

// Task to print temperature and humidity via UART
void StartUARTTask(void *argument) {
    float readings[2];
    for (;;) {
        if (osMessageQueueGet(sensorQueueHandle, &readings, NULL, osWaitForever) == osOK) {
            printf("Temperature: %.2f °C, Humidity: %.2f %%\r\n", readings[0], readings[1]);
        }
    }
}



